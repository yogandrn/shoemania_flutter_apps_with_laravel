class GalleryController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('gallery.index', [
            'title' => 'Gallery',
            'galleries' => Gallery::with(['product'])->paginate(10),
        ]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('gallery.create', [
            'title' => 'Add Gallery',
            'products' => Product::where('deleted_at', null)->get()
        ]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(GalleryRequest $request)
    {
        $data = $request->all();

        $data['image_url'] = $request->file('image_url')->store('assets/products/' . date('Y') . "/", 'public');


        Gallery::create($data);

        return redirect()->route('dashboard.gallery.index')->with('success', 'Berhasil menambahkan galleri produk!');
    }

